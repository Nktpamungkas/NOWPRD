SELECT
	SUBSTRING(ID_NO, 1, 8) AS PRODUCTIONORDER
--	,*
FROM
	TICKET.dbo.TICKET_DETAIL
WHERE
	PRODUCT_CODE IN ('E-1-009')
	AND LEN(ID_NO) > 9
	AND (ACTUAL_WT IS NULL OR ACTUAL_WT = '0')
--	AND SUBSTRING(ID_NO, 1, 8) = '00194454'
ORDER BY 
	ID_NO DESC
	
	
UPDATE
	TICKET.dbo.TICKET_DETAIL
SET ACTUAL_WT = '160',
	COMP_DATE = '20250306',
	COMP_TIME = '20:57:01'	
WHERE
	PRODUCT_CODE IN ('E-1-009')
	AND LEN(ID_NO) > 9
	AND ACTUAL_WT IS NULL
	AND SUBSTRING(ID_NO, 1, 8) = '00194220'