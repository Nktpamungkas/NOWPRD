SELECT
	r2.GROUPNUMBER,
	r2.GROUPTYPECODE,
	r2.LINETYPE,
	r2."SEQUENCE",
	r2.SUBSEQUENCE,
	r2.ITEMTYPEAFICODE,
	r2.SUBCODE01,
	r2.SUFFIXCODE 
FROM	
	RECIPE r 
LEFT JOIN RECIPECOMPONENT r2 ON r2.RECIPENUMBERID = r.NUMBERID AND r2.SUFFIXCODE = '001'
LEFT JOIN RECIPE r3 ON r3.SUBCODE01 = r2.SUBCODE01 AND r3.SUFFIXCODE = r2.SUFFIXCODE 
WHERE 
--	r.SUBCODE01 = '20080/240484D/NILO'
	r.SUBCODE01 = '7091/249999D/R'
	AND r.SUFFIXCODE = '001'