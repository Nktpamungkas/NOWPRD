	DISTINCT 
    v.PRODUCTIONORDERCODE,
    p2.OPERATIONCODE,
	p.PROPROGRESSPROGRESSNUMBER,
	p.DEMANDSTEPSTEPNUMBER,
	p2.PROGRESSTEMPLATECODE,
	(p2.PROGRESSPARTIALENDTIME || ' ' || p2.PROGRESSPARTIALENDTIME) AS MULAI
